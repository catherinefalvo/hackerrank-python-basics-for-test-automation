# Le a primeira entrada e converte para inteiro
a = int(input())

# Le a segunda entrada e converte para inteiro
b = int(input())

# Soma os dois numeros e imprime o resultado
print(a + b)

# Subtrai o segundo numero do primeiro e imprime o resultado
print(a - b)

# Multiplica os dois numeros e imprime o resultado
print(a * b)
