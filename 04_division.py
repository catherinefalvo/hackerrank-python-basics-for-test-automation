# Le a primeira entrada e converte para inteiro
a = int(input())

# Le a segunda entrada e converte para inteiro
b = int(input())

# Imprime o resultado da divisao inteira (sem casas decimais)
print(a // b)

# Imprime o resultado da divisao real (com casas decimais)
print(a / b)
